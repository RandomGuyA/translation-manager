<h1>DaRK SKY ambassadors</h1>
<div>Since our Reserve was first designated, many businesses in the park have worked with the National Park Authority and Brecon Beacons Park Society to embrace our night skies. Over 50 businesses to date are now trained as Dark Sky Ambassadors, full of knowledge about the night skies above our National Park and how you can enjoy them. They have also undertaken a three day National Park training programme so have a real insight into our National Park.</div>
